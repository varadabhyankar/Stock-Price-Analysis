const itemsPerPage = 20;
let currentPage = 1;

function searchCompanies() {
    const searchInput = document.getElementById('searchInput').value.toLowerCase();
    const companyList = document.getElementById('companyList');
    companyList.innerHTML = '';

    // Filter companies based on search input
    const filteredCompanies = companies.filter(company =>
        company.name.toLowerCase().includes(searchInput) || company.symbol.toLowerCase().includes(searchInput)
    );

    // Calculate total pages
    const totalPages = Math.ceil(filteredCompanies.length / itemsPerPage);

    // Display companies for the current page
    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    const displayCompanies = filteredCompanies.slice(startIndex, endIndex);

    displayCompanies.forEach(company => {
        const companyCard = document.createElement('div');
        companyCard.className = 'company-card';
        companyCard.innerHTML = `<strong>${company.name}</strong><br>${company.symbol}`;
        companyList.appendChild(companyCard);
        const graphType = 'longTerm'
        companyCard.addEventListener('click', () => {
            
            window.location.href = `company_details?name=${encodeURIComponent(company.name)}`+`&symbol=${encodeURIComponent(company.symbol)}`+`&graphType=${encodeURIComponent(graphType)}`;
        });
    });

    // Add pagination buttons
    addPaginationButtons(totalPages);
}

function addPaginationButtons(totalPages) {
    const paginationContainer = document.getElementById('paginationContainer');
    paginationContainer.innerHTML = '';

    for (let page = 1; page <= totalPages; page++) {
        const button = document.createElement('button');
        button.innerText = page;
        button.addEventListener('click', () => {
            currentPage = page;
            searchCompanies();
        });
        paginationContainer.appendChild(button);
    }
}